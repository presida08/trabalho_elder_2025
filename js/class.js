class Horario {
    constructor(id, hora, domingo, segunda, terca, quarta, quinta, sexta, sabado) {
      this.id = id;
      this.hora = hora;
      this.domingo = domingo;
      this.segunda = segunda;
      this.terca = terca;
      this.quarta = quarta;
      this.quinta = quinta;
      this.sexta = sexta;
      this.sabado = sabado;
    }
}